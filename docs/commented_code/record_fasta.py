from dataclasses import dataclass

@dataclass
class SequenceRecordFasta:
    """
    Представляет запись биологической последовательности с уникальным идентификатором.
    
    Этот класс используется для хранения и обработки биологических последовательностей
    (например, ДНК, РНК или белков) вместе с их идентификаторами. Реализует базовые
    операции, такие как получение длины последовательности.

    Атрибуты:
        id (str): Уникальный идентификатор последовательности (например, номер доступа в базе данных)
        sequence (str): Строковое представление биологической последовательности

    Примеры:
        >>> record = SequenceRecord("seq_1", "ATCG")
        >>> print(record.id)
        seq_1
        >>> print(len(record))
        4
    """
    id: str
    sequence: str

    def __len__(self):
        """
        Возвращает длину биологической последовательности.

        Реализует встроенную функцию len() для объектов SequenceRecord.
        Подсчитывает количество символов в строке последовательности.

        Возвращает:
            int: Длина последовательности (количество символов)

        Примеры:
            >>> record = SequenceRecord("seq1", "ACGT")
            >>> len(record)
            4
            >>> record = SequenceRecord("empty", "")
            >>> len(record)
            0
        """
        return len(self.sequence)